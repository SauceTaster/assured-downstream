def render_release_workflow(
    profile: dict[str, Any],
    *,
    root: Path,
    pins: dict[str, Any],
    execute: bool = False,
    force: bool = False,
) -> RenderResult:
    root = root.resolve()
    if not root.exists():
        raise FileNotFoundError(root)
    if not root.is_dir():
        raise NotADirectoryError(root)

    release = validate_release_profile(profile)
    required = require_pins(
        normalize_pin_map(pins),
        release.get("required_actions") or [],
    )
    if required is None:
        return RenderResult(
            written=[],
            skipped=[
                {
                    "id": "attested-release-workflow",
                    "reason": "release workflow rendering requires full SHA pins for all required actions",
                }
            ],
        )

    relative_path = release["workflow_path"]
    target = root / relative_path
    resolved_target = target.resolve()
    if not resolved_target.is_relative_to(root):
        raise ValueError("Release workflow path escapes the repository root")
    for parent in (target, *target.parents):
        if parent == root.parent:
            break
        if parent.exists() and parent.is_symlink():
            raise ValueError("Release workflow path traverses a symlink")
    if target.exists() and not force:
        return RenderResult(
            written=[],
            skipped=[
                {
                    "id": "attested-release-workflow",
                    "reason": f"{relative_path} already exists; pass --force to overwrite",
                }
            ],
        )

    content = attested_release_workflow(profile, required)
    if execute:
        try:
            write_text_confined(
                root,
                relative_path,
                content,
                force=force,
            )
        except FileExistsError:
            return RenderResult(
                written=[],
                skipped=[
                    {
                        "id": "attested-release-workflow",
                        "reason": f"{relative_path} appeared during rendering; retry with --force to overwrite",
                    }
                ],
            )

    return RenderResult(
        written=[{"id": "attested-release-workflow", "path": relative_path}],
        skipped=[],
    )


