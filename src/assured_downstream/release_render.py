def validate_release_profile(profile: dict[str, Any]) -> dict[str, Any]:
    release = profile.get("release")
    if not isinstance(release, dict):
        raise ValueError("Release profile has no release configuration")

    require_safe_path(
        release.get("workflow_path"),
        label="release workflow",
        prefix=(".github", "workflows"),
        pattern=FILE_PATH_PATTERN,
        suffixes=(".yml", ".yaml"),
    )
    require_safe_path(
        release.get("sbom_path"),
        label="SBOM output",
        prefix=("assured-evidence",),
        pattern=FILE_PATH_PATTERN,
    )
    artifact_paths = release.get("artifact_paths")
    if not isinstance(artifact_paths, list) or not artifact_paths:
        raise ValueError("Release artifact paths must be a non-empty list")
    for artifact_path in artifact_paths:
        require_safe_path(
            artifact_path,
            label="release artifact",
            prefix=("dist",),
            pattern=ARTIFACT_PATTERN,
        )
    require_pattern(release.get("runs_on"), RUNNER_PATTERN, label="runner")
    require_pattern(
        release.get("confirmed_tag_pattern"),
        TAG_PATTERN,
        label="release tag pattern",
    )
    if release.get("sbom_format") != "spdx-json":
        raise ValueError("Release SBOM format must be spdx-json")
    required_actions = release.get("required_actions")
    if (
        not isinstance(required_actions, list)
        or not all(isinstance(action, str) for action in required_actions)
        or not TEMPLATE_ACTIONS.issubset(set(required_actions))
    ):
        raise ValueError("Release profile omits a required pinned action")
    return release


def require_safe_path(
    value: Any,
    *,
    label: str,
    prefix: tuple[str, ...],
    pattern: re.Pattern[str],
    suffixes: tuple[str, ...] | None = None,
) -> str:
    if not isinstance(value, str) or pattern.fullmatch(value) is None:
        raise ValueError(f"{label.capitalize()} path contains unsafe characters")
    path = PurePosixPath(value)
    if path.is_absolute() or not path.parts or ".." in path.parts:
        raise ValueError(f"{label.capitalize()} path escapes its allowed root")
    if path.parts[: len(prefix)] != prefix:
        raise ValueError(
            f"{label.capitalize()} path must start with {'/'.join(prefix)}"
        )
    if suffixes is not None and not value.endswith(suffixes):
        raise ValueError(f"{label.capitalize()} path has an unsupported extension")
    return value


def require_pattern(value: Any, pattern: re.Pattern[str], *, label: str) -> str:
    if not isinstance(value, str) or pattern.fullmatch(value) is None:
        raise ValueError(f"{label.capitalize()} contains unsafe characters")
    return value


def release_tag_trigger(profile: dict[str, Any]) -> str:
    if not release_profile_confirmed(profile):
        return ""
    release = profile.get("release") or {}
    pattern = release.get("confirmed_tag_pattern") or "secure-v*"
    return f"""  push:
    tags:
      - '{pattern}'"""


def release_profile_confirmed(profile: dict[str, Any]) -> bool:
    review = profile.get("review") or {}
    return bool(
        review.get("release_workflow_confirmed") is True
        and review.get("artifact_paths_confirmed") is True
        and review.get("isolated_builder_confirmed") is True
        and review.get("lineage_confirmed") is True
        and lineage_is_valid(profile.get("lineage"))
    )


def lineage_verification_commands(profile: dict[str, Any]) -> list[str]:
    if not release_profile_confirmed(profile):
        return [
            "echo 'source lineage is not fully confirmed; refusing to fetch or build' >&2",
            "exit 1",
        ]
    return [
        "set -eu",
        'git fetch --no-tags --depth=1 "https://github.com/${SOURCE_REPOSITORY}.git" "$UPSTREAM_REF"',
        'test "$(git rev-parse FETCH_HEAD)" = "$UPSTREAM_REF"',
        'git merge-base --is-ancestor "$UPSTREAM_REF" "$GITHUB_SHA"',
    ]


def isolated_build_commands(profile: dict[str, Any]) -> list[str]:
    release = profile.get("release") or {}
    builder = release.get("isolated_builder") or {}
    review = profile.get("review") or {}
    if review.get("isolated_builder_confirmed") is not True:
        return [
            "echo 'isolated builder is not confirmed; refusing to execute upstream code' >&2",
            "exit 1",
        ]
    if not release_profile_confirmed(profile):
        return [
            "echo 'release profile or source lineage is not fully confirmed; refusing to execute upstream code' >&2",
            "exit 1",
        ]
    image = builder.get("image")
    digest = builder.get("image_digest")
    run_as = builder.get("run_as")
    argv = builder.get("command_argv")
    if (
        not isinstance(image, str)
        or BUILDER_IMAGE_PATTERN.fullmatch(image) is None
        or not isinstance(digest, str)
        or BUILDER_DIGEST_PATTERN.fullmatch(digest) is None
        or not isinstance(run_as, str)
        or RUN_AS_PATTERN.fullmatch(run_as) is None
        or builder.get("network") != "none"
        or not isinstance(argv, list)
        or not argv
        or not all(
            isinstance(value, str)
            and bool(value)
            and "\n" not in value
            and "\x00" not in value
            for value in argv
        )
    ):
        return [
            "echo 'isolated builder configuration is invalid; refusing to execute upstream code' >&2",
            "exit 1",
        ]
    image_ref = f"{image}@sha256:{digest}"
    command = " ".join(shlex.quote(value) for value in argv)
    return [
        "install -d -m 0777 dist",
        f"docker pull {shlex.quote(image_ref)}",
        (
            "docker run --rm --network none --read-only --cap-drop ALL "
            "--security-opt no-new-privileges --pids-limit 256 --memory 2g "
            f"--cpus 2 --user {shlex.quote(run_as)} "
            '-e HOME=/tmp/home -v "$GITHUB_WORKSPACE:/src:ro" '
            '-v "$GITHUB_WORKSPACE/dist:/out:rw" '
            "--tmpfs /tmp:rw,noexec,nosuid,size=1g "
            f"{shlex.quote(image_ref)} {command}"
        ),
    ]


def lineage_is_valid(value: Any) -> bool:
    if not isinstance(value, dict):
        return False
    source = value.get("source_full_name")
    upstream_ref = value.get("upstream_ref")
    return bool(
        isinstance(source, str)
        and REPOSITORY_PATTERN.fullmatch(source) is not None
        and all(part not in {".", ".."} for part in source.split("/"))
        and isinstance(upstream_ref, str)
        and len(upstream_ref) == 40
        and all(character in "0123456789abcdef" for character in upstream_ref)
    )


def indent_commands(lines: list[str], spaces: int) -> str:
    prefix = " " * spaces
    return "\n".join(f"{prefix}{line}" for line in lines)


def indent_text(value: str, spaces: int) -> str:
    prefix = " " * spaces
    return "\n".join(f"{prefix}{line}" for line in value.splitlines())


def write_text_confined(
    root: Path,
    relative_path: str,
    content: str,
    *,
    force: bool,
) -> None:
    parts = PurePosixPath(relative_path).parts
    directory_flags = os.O_RDONLY | os.O_DIRECTORY
    if hasattr(os, "O_NOFOLLOW"):
        directory_flags |= os.O_NOFOLLOW
    root_fd = os.open(root, directory_flags)
    directory_fd = root_fd
    temporary_name: str | None = None
    try:
        for part in parts[:-1]:
            try:
                os.mkdir(part, mode=0o755, dir_fd=directory_fd)
            except FileExistsError:
                pass
            next_fd = os.open(part, directory_flags, dir_fd=directory_fd)
            if directory_fd != root_fd:
                os.close(directory_fd)
            directory_fd = next_fd

        temporary_name = f".{parts[-1]}.{uuid.uuid4().hex}.tmp"
        temporary_flags = os.O_WRONLY | os.O_CREAT | os.O_EXCL
        if hasattr(os, "O_NOFOLLOW"):
            temporary_flags |= os.O_NOFOLLOW
        temporary_fd = os.open(
            temporary_name,
            temporary_flags,
            0o644,
            dir_fd=directory_fd,
        )
        try:
            with os.fdopen(
                temporary_fd, "w", encoding="utf-8", closefd=False
            ) as handle:
                handle.write(content)
                handle.flush()
                os.fsync(handle.fileno())
        finally:
            os.close(temporary_fd)

        if force:
            os.replace(
                temporary_name,
                parts[-1],
                src_dir_fd=directory_fd,
                dst_dir_fd=directory_fd,
            )
        else:
            os.link(
                temporary_name,
                parts[-1],
                src_dir_fd=directory_fd,
                dst_dir_fd=directory_fd,
                follow_symlinks=False,
            )
            os.unlink(temporary_name, dir_fd=directory_fd)
        os.fsync(directory_fd)
        temporary_name = None
    except OSError as exc:
        if exc.errno in {errno.ELOOP, errno.ENOTDIR}:
            raise ValueError(
                "Release workflow path traverses a symlink or non-directory"
            ) from exc
        raise
    finally:
        if temporary_name is not None:
            try:
                os.unlink(temporary_name, dir_fd=directory_fd)
            except FileNotFoundError:
                pass
        if directory_fd != root_fd:
            os.close(directory_fd)
        os.close(root_fd)
